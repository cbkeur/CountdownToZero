#import "UIColor+Carter.h"

@implementation UIColor (Carter)

+ (UIColor *)carterBackgroundColor
{
    return [UIColor colorWithRed:237/255.0 green:233/255.0 blue:224/255.0 alpha:1]; // 244 247 251 37 33 24
}

+ (UIColor *)carterRedColor
{
    return [UIColor colorWithRed:205/255.0 green:67/255.0 blue:22/255.0 alpha:1];
}

+ (UIColor *)carterBlueColor
{
    return [UIColor colorWithRed:0 green:79/255.0 blue:119/255.0 alpha:1];
}

@end
